import math
a = float(input('Digite um angulo: '))
conseno = math.cos(math.radians(a))
seno = math.sin(math.radians(a))
tangente = math.sin(math.radians(a))
print(f'O valor do coseno de {a} e igual: {conseno}')
print(f'O valor do seno de {a} e igual: {seno}')
print(f'O Valor da tangente de {a} e igual: {tangente}')